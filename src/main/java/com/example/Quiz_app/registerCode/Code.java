package com.example.Quiz_app.registerCode;

/**
 * Class responsible for providing response code for CodeApi class.
 */
public class Code {
    int Code;

    /**
     * Getter returning response code.
     * @return - response code.
     */
    public int getCode() {
        return Code;
    }

    /**
     * Setter providing ability to change response code.
     * @param code - new response code.
     */
    public void setCode(int code) {
        Code = code;
    }
}
